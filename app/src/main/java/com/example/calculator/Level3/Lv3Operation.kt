package com.example.calculator.Level3

class AddOperation() {
    fun operate(num:Int,num2:Int) : Int{
        return num + num2
    }
}

class SubOperation() {
    fun operate(num:Int,num2:Int) : Int{
        return num - num2
    }

}

class MulOperation() {
    fun operate(num:Int,num2:Int) : Int{
        return num * num2
    }
}

class DivOperation() {
    fun operate(num:Int,num2:Int) : Int{
        return num / num2
    }
}